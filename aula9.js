// + , - , / , * , **

let salario = 100;

console.log(salario+salario);
console.log(salario-salario);
console.log(salario*salario);
console.log(salario/salario);
console.log(5 ** 5); // exponencial

// operadores de decremento e incremento = ( -- , ++);

let idade = 18;

console.log(++idade); // Ja imprime incrementado 
console.log(idade++); // imprime e depois incrementa 

console.log(--idade); 
console.log(idade--); 